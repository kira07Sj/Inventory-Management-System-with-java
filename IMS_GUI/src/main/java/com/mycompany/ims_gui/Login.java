    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
     if(LoginUserNameField.getText().equals("") || LoginPasswordField.getText().equals(""))
     {
     JOptionPane.showMessageDialog(null, "Please Enter Username and Password to Login!");
     }
     else{
     try{
     String sql = "select * from users where UserName = ? and PassWord= ?";
     ps = conn.prepareStatement(sql);
     ps.setString(1,LoginUserNameField.getText());
     ps.setString(1,LoginPasswordField.getText());
     rs = ps.executeQuery();
     
     if(rs.next())
     {
     new IMS_UI().setVisible(true);
     this.dispose();
     }
     else
     {
     JOptionPane.showMessageDialog(null, "Incorrect Username or Passowrd");
     LoginUserNameField.setText("");
     LoginPasswordField.setText("");
     
     }
     
     } catch(Exception e)
     {
     JOptionPane.showMessageDialog(null, e);
     }
     }
      
    }//GEN-LAST:event_loginBtnActionPerformed

